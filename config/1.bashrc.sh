# =============================================================================================================
# SSH Configuration
# =============================================================================================================

eval "$(ssh-agent -s)"
#ssh-add ~/.ssh/debs
ssh-add ~/.ssh/work
#ssh-add ~/.ssh/rasberry
#ssh-add ~/.ssh/render

# =============================================================================================================
# Interactive Shell Settings
# =============================================================================================================

case $- in
    *i*) ;;
      *) return;;
esac


# =============================================================================================================
# History Settings
# =============================================================================================================

HISTCONTROL=ignoreboth
shopt -s histappend
HISTSIZE=1000
HISTFILESIZE=2000

# =============================================================================================================
# Window Size Handling
# =============================================================================================================

shopt -s checkwinsize

# =============================================================================================================
# Chroot Variable
# =============================================================================================================

if [ -z "${debian_chroot:-}" ] && [ -r /etc/debian_chroot ]; then
    debian_chroot=$(cat /etc/debian_chroot)
fi

# =============================================================================================================
# Prompt Settings
# =============================================================================================================

case "$TERM" in
    xterm-color|*-256color) color_prompt=yes;;
esac

force_color_prompt=yes

if [ -n "$force_color_prompt" ]; then
    if [ -x /usr/bin/tput ] && tput setaf 1 >&/dev/null; then
        color_prompt=yes
    else
        color_prompt=
    fi
fi

if [ "$color_prompt" = yes ]; then
    PS1='\[\033[01;31m\][\[\033[38;2;133;237;37m\]sys\[\033[01;31m\]@\[\033[1;36m\]parrot\[\033[01;31m\]]\[\033[00m\]-\[\033[01;31m\][\[\033[38;2;133;237;37m\]\w\[\033[01;31m\]]\[\033[00m\]\$ '
else
    PS1='\[\033[01;31m\][\[\033[38;2;133;237;37m\]sys\[\033[01;31m\]@\[\033[1;36m\]parrot\[\033[01;31m\]]\[\033[00m\]-\[\033[01;31m\][\w]\$ '
fi


unset color_prompt force_color_prompt

case "$TERM" in
xterm*|rxvt*)
    PS1="\[\e]0;${debian_chroot:+($debian_chroot)}\u@\h: \w\a\]$PS1"
    ;;
*)
    ;;
esac

# =============================================================================================================
# Color Support and Aliases
# =============================================================================================================

if [ -x /usr/bin/dircolors ]; then
    test -r ~/.dircolors && eval "$(dircolors -b ~/.dircolors)" || eval "$(dircolors -b)"
    alias ls='ls --color=auto'
fi

# =============================================================================================================
# Alias Definitions
# =============================================================================================================

if [ -f ~/.bash_aliases ]; then
    . ~/.bash_aliases
fi

# =============================================================================================================
# Programmable Completion Features
# =============================================================================================================

if ! shopt -oq posix; then
  if [ -f /usr/share/bash-completion/bash_completion ]; then
    . /usr/share/bash-completion/bash_completion
  elif [ -f /etc/bash_completion ]; then
    . /etc/bash_completion
  fi
fi

# =============================================================================================================
# NVM Configuration
# =============================================================================================================

export NVM_DIR="$HOME/.nvm"
[ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh"
[ -s "$NVM_DIR/bash_completion" ] && \. "$NVM_DIR/bash_completion"



export PATH=$PATH:/home/system-1/Bin


